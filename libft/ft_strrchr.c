#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*tmp;

	tmp = NULL;
	while (1)
	{
		if (*s == c)
			tmp = (char *)s;
		if (*s++ == '\0')
			return (tmp);
	}
}
